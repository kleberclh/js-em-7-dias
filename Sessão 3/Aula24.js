let num1 = 3;
let num2 = 2;

let total = num1 + num2;

console.log(num1 + num2); // 5
console.log(num1 - num2); // 1
console.log(num1 * num2); // 6
console.log(num1 / num2); // 1.5

console.log(num1 % num2); // 1
console.log(num1 ** num2); // 9

console.log(++total); // 6
console.log(--total); // 5

/* Valor so aparece apos realizar o console.log(total) pos armazenou o valor quando colocou   console.log(total++) */

console.log(total++); // 5
console.log(total); // 6

console.log(total--); // 6
console.log(total); // 4
